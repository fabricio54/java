package operadores;

public class Operadores {
    public static void main(String[] args){
        int numero1 = 10;
        int numero2 = 20;
        int soma = numero1 + numero2;
        // operador de concatenação
        System.out.println(" A concatenação e " +numero1+numero2);
        // operador de soma
        System.out.println(" A soma e "+soma);
        // operador de divisão
        System.out.println(" A divisão e "+(numero2/numero1));
        // resto da divisão
        System.out.println(" O resto da divisão e "+(numero2%numero1));
    }
}
